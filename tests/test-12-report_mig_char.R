test_that("test calcule method",{
	  require(stacomiR)
	  stacomi(gr_interface=FALSE,login_window=FALSE,database_expected=TRUE)
	  # overriding user schema to point to iav
	  assign("sch","logrami.",envir_stacomi)
	  r_mig_char<-new("report_mig_char")
	  baseODBC<-get("baseODBC",envir=envir_stacomi)
	  baseODBC[c(2,3)]<-rep("logrami",2)
	  assign("baseODBC",baseODBC,envir_stacomi)
	  sch<-get("sch",envir=envir_stacomi)
	  assign("sch","logrami.",envir_stacomi)
	  r_mig_char<-choice_c(r_mig_char,
		  dc=c(107,108,101),
		  taxa=c("Salmo salar"),
		  stage=c('5','11','BEC','BER','IND'),
		  parquan=c('A124','C001'),
		  parqual=NULL,
		  horodatedebut="2012-01-01",
		  horodatefin="2012-12-31",
		  silent=TRUE)
	  r_mig_char<-connect(r_mig_char)
	  r_mig_char<-calcule(r_mig_char,silent=TRUE)
	  expect_true(nrow(r_mig_char@calcdata)>0, label="Test that calcule method worked")
	  expect_true(all(is.na(r_mig_char@calcdata$car_par_code_qual)), label="Test that calcule method does not return any qualitative result in absence of qualitative parm")
	  
	  # now with a qualitative parm
	  r_mig_char<-setasqualitative(r_mig_char,
		  par='A124',
		  silent=TRUE,breaks=c(0,1.5,2.5,10),label=c("age 1","age 2","age 3"))
	  r_mig_char<-calcule(r_mig_char,silent=TRUE)
	  expect_true(any(!is.na(r_mig_char@calcdata$car_par_code_qual))&any(!is.na(r_mig_char@calcdata$car_par_code_quan)), label="The merge function works and returns both qualitative and quantitative parameters")
        rm(list=ls(envir=envir_stacomi),envir=envir_stacomi)
	})


